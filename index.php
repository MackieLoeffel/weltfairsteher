                        <b>
                        <h3  class="abstaende" id="wie" style="color: #C5F2B6; background-color: #CC4321;">Was kann man gewinnen?
                        </h3></b>

                        <div class="abstaende"  style="text-align: justify; background-color: #1BAB3F; padding: 20px;">

Keine Sorge, mit leeren Händen geht am Ende keine Klasse nach Hause. Ganz im Gegenteil: Schon während des Schuljahres verfolgt ihr Etappenziele.
Jede Klasse sieht in der Tabelle, wie viele Punkte sie noch von der nächsten Etappe entfernt ist. Habt ihr eine Etappe erreicht, so schicken
wir euch zur Belohnung eine kleine Überraschung per Post. Außerdem werdet ihr über die Zeit der Teilnahme hinweg
so manche Errungenschaft feiern, die
wortwörtlich nachhaltig ist - für´s Leben lernen also. <br>
<a href="javascript:void(0)" onclick="return toggleMe('fuenf')" style="text-align: center; color: #C5F2B6; background-color: #108542">Weiterlesen...</a>
<span id="fuenf" style="display:none;">
Ob ihr in der Tabelle gerade unten oder oben steht, ist demzufolge nur zweitrangig.
Letztendlich spielen nämlich alle Klassen gemeinsam für eine lebenswerte Zukunft, die fair, also gerecht, gestaltet ist.
Na gut, ein paar Überraschungen haben wir für die Klassen mit den meisten Punkten trotzdem parat - als zusätzlicher
Ansporn sozusagen.
<br><br>
Wenn ihr am Ende nicht einen der drei ersten Plätze belegen solltet, so werdet ihr trotzdem
einen kleinen Preis samt Urkunde erhalten, denn selbst wer am Tabellenende steht ist deshalb nicht "un-nachhaltig"
beziehungsweise zu schlecht oder ungeeignet für Nachhaltigkeit!
Am wichtigsten ist der erste Schritt. Und dafür müsst ihr euch nur anmelden.
</span>
</div><br>
<b>
<h3 class="abstaende" id="spenden" style="color: #C5F2B6; background-color: #10B3B3;">Wie meldet man sich an?</h3></b>

<div class="abstaende"  style="text-align: justify; background-color: #1BAB3F; padding: 20px;">
    Die hauptverantwortliche Lehrkraft schickt einfach eine E-Mail an  <a href="mailto:kontakt@weltfairsteher.jetzt" target="_top">kontakt@weltfairsteher.jetzt</a> und teilt uns den kreativen Klassennamen mit,
    den ihr euch selbst ausdenken könnt. Das kann so etwas wie "Die Sojapatronen" sein, oder "Warme Kürbissuppen".
    Wenn ihr uns verratet, von welcher Schule ihr kommt, würden wir uns natürlich sehr freuen - schon alleine deshalb,
    weil wir euch eure Etappen-Überraschungen zusenden möchten. Deshalb solltet ihr uns auch mitteilen, wie viele Schüler*innen unter
    eurem Klassennamen teilnehmen.
    Keine Sorge: Eure Schuldaten werden nicht veröffentlicht.
    <br>
    <a href="javascript:void(0)" onclick="return toggleMe('sechs')" style="text-align: center; color: #C5F2B6; background-color: #108542">Weiterlesen...</a>
    <span id="sechs" style="display:none;">
Die Lehrkraft, die die Klasse anmeldet, erhält anschließend eine E-Mail mit den persönlichen Zugangsdaten.
Damit kann die Lehrerin beziehungsweise der Lehrer über den Reiter <i>Lehrer-Bereich</i> gewissenhaft bestandene Challenges
eintragen oder eine Selfmade-Challenge vorschlagen. Außerdem haben eingeloggte Lehrkräfte Zugang zu
ausführlichen Beschreibungen und Unterrichtsmaterialien für die einzelnen Challenges.

    Übrigens: Eine Lehrkraft kann auch die Verantwortung für zwei Klassen übernehmen. <b>Die Anmeldefrist für das Schuljahr 2016/17 endet am 04. Oktober 2016.</b>
</span>
