                        <b>

                        <h3 id="worum"class="abstaende" style="color: #C5F2B6; background-color: #C9960A;">Was ist das?</h3>
                        </b>

                        <div  class="abstaende" style="text-align: justify; background-color: #1BAB3F; padding: 20px;">
                          WeltFAIRsteher ist eine Nachhaltigkeitschallenge für Schulklassen, also ein Wettbewerb, bei dem es darum geht,
                        gemeinsam als Klasse Aufgaben zu bewältigen, die mit Nachhaltigkeit zu tun haben. Das kann von kleinen handwerklichen Projekten über
                        die Organisation und Verspeisung eines ökologisch nachhaltigen Frühstücks bis hin zu unterhaltsamen Spielen,
                        spannenden Referaten und sozialem Engagement reichen.

                            </div><br>
                        <b>
                        <h3  class="abstaende" id="wer" style=" color: #C5F2B6; background-color: #3A7EFC;">Worum geht´s?</h3>
                        </b>

                        <div class="abstaende" style="text-align: justify; background-color: #1BAB3F; padding: 20px;">
                        Nachhaltigkeit ist ein sehr vielseitiges Thema, das in fast jeden Lebensbereich dringt. Die wichtigsten Aspekte von Nachhaltigkeit
                        sind:<ul>
                        <li> <b>Ökologie</b>, also die Beziehungen von Organismen wie Menschen, Tieren oder Pflanzen zu ihrer Umwelt,</li>
                      <li>  <b>Soziales</b>, also die Art und Weise, wie Menschen
                        in der Gesellschaft zusammenleben und</li>
                        <li> <b>Wirtschaft</b>, also Bereiche wie Warenproduktion, Einkauf und Verkauf.</li></ul>
                        <a href="javascript:void(0)" onclick="return toggleMe('zwei');" style="text-align: center; color: #C5F2B6; background-color: #108542">Weiterlesen...</a>
                        <span id="zwei" style="display:none;">  Mit all diesen Dingen
                        habt ihr - ohne es immer zu bemerken - tagtäglich zu tun. Selbst hinter kleinen Handlungen steckt dabei häufig ein riesiger Prozess, der zuvor stattfand.
                        Wusstet ihr zum Beispiel, das
                        vor dem Kauf einer einfachen elektrischen Zahnbürste viele Rohstoffe wie Metalle oder Öle über mehrere Kontinente transportiert werden? Schiffe, Züge
                        und Lastwägen legen also tausende Kilometer zurück, damit so eine elektrische Zahnbürste für einen möglichst niedrigen Preis im Geschäft steht.
                        <br><br>
                        Das bringt aber viele Probleme mit sich. Nur eines davon sind die vielen Treibhausgase, die die Schiffe und Lastwägen ausstoßen.
                        Das ist schlecht für die Umwelt
                        und beschleunigt die Erderwärmung. Solche Probleme gibt es an vielen Orten unserer Welt. Als Teilnehmende an WeltFAIRsteher lernt ihr einige davon kennen und
                        erfahrt viele Lösungsvorschläge, wie die Probleme beseitigt werden könnten. Nachhaltigkeit kann nämlich eigentlich jeder. Man muss nur das Glück haben,
                        zur richtigen Zeit und am richtigen Ort die richtigen Erfahrungen zu machen. Und dabei helfen wir euch.
                              </span></div><br>
                        <b>
                        <h3   class="abstaende" id="ablauf" style="color: #C5F2B6; background-color: #7761C5;">Wer kann mitmachen?</h3>
                        </b>

                        <div class="abstaende"  style="text-align: justify;background-color: #1BAB3F; padding: 20px;">
                            Teilnehmen kann jede 6. oder 7. Klasse einer öffentlichen oder privaten Schule in Bayern.
Besonders vorteilhaft ist es natürlich, eine motivierte Lehrkraft zu haben, mit der man an <i>WeltFAIRsteher</i> teilnimmt.
Wenn ihr eine Lehrkraft mit einem Korb Äpfeln oder einem Sack Kartoffeln bestechen wollt, ist das also vielleicht nicht die beste Voraussetzung -
sofern die Lehrerin beziehungsweise der Lehrer nicht nur zu beschäftigt war und sich eigentlich für Nachhaltigkeit interessiert.
Übrigens: Eine Klasse kann natürlich auch mit der Unterstützung mehrerer Lehrkräfte teilnehmen, etwa um sowohl im Erdkunde-, als auch im Biologie-Unterricht an Challenges zu arbeiten.
Anmelden kann euch aber nur eine Lehrkraft.
                        </div><br>

                        <b>
                        <h3  class="abstaende" id="gewinn" style="color: #C5F2B6; background-color: #1B5A0A;">Wie läuft´s ab?</h3>
                        </b>

                        <div class="abstaende"  style="text-align: justify; background-color: #1BAB3F; padding: 20px;">
Jede Klasse kann sich an den Aufgaben, die ihr unter dem Reiter <i>Challenges</i> findet, versuchen. Mit welcher Challenge ihr dabei beginnt ist
genauso gleichgültig wie die Anzahl der Schüler*innen, die sich an einer Challenge beteiligen. Manchmal ist es sogar am besten,
wenn nur wenige Schüler*innen eine Aufgabe verfolgen. Sobald ihr eine Challenge erfolgreich abgeschlossen habt,
kann eure Lehrkraft die Challenge im <i>Lehrer-Bereich</i> eintragen.<br>
